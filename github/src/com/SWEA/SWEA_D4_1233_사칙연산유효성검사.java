	public static void main(String[] args) throws NumberFormatException, IOException {
		input = new BufferedReader(new StringReader(src));
		for(int t=1; t<=10; t++) {
			answer = 1;// 성공 시
			N = Integer.parseInt(input.readLine());
			
			nodes = new Node[N+1]; //1부터 시작이므로 +1
			for(int i=1; i<=N; i++) { //인덱스를 맞추기위해 1부터 시작
				tokens = new StringTokenizer(input.readLine());
				//순서는 버림
				tokens.nextToken();
				
				String val = tokens.nextToken();
				// 자식이 있을 수도 있고 없을 수도 있다.
				Integer left =null, right = null;
				if(tokens.hasMoreTokens()) { // 다음 토큰이 있는지 검사. 있으면 참 없으면 거짓
					left = Integer.valueOf(tokens.nextToken());
				}
				if(tokens.hasMoreTokens()) { // 다음 토큰이 있는지 검사. 있으면 참 없으면 거짓
					right = Integer.valueOf(tokens.nextToken());
				}
				nodes[i] = new Node(val,left,right);
			}
			//입력처리 완료
			postOrder(1);
			output.append("#").append(t).append(" ").append(answer).append("\n");
		}
		System.out.println(output);
	}
	static String oper = "+-*/";
	
	static void postOrder(Integer nodeIdx) {
		// 갈 필요가 있을때만 가보자
		if(nodeIdx!=null && answer==1) {
			Node node = nodes[nodeIdx];
			postOrder(node.left);
			postOrder(node.right);
			
			// 내꺼 사용하기
			if(oper.contains(node.value)) {
				if(node.left == null || node.right == null) { // 이항연산자 사용중인데 항이 2개가 아니라면
					answer = 0; //실패
					return;
				}else {
					Node left = nodes[node.left];
					Node right = nodes[node.right];
					
					if(left == null || right==null) {
						answer = 0;
						return;
					}
					if(oper.contains(left.value) || oper.contains(right.value)) {
						answer = 0;
								return;
					}
					// 계산 가능
					node.value = "0";
				}
			}else {
				// 연산자가 아니면 단말이어야 한다. --> 자식이 있으면..ㅅㅂ
				if(node.left!=null || node.right!=null) {
					answer=0;
					return;
				}
			}
		}
	}
	
